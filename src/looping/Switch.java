package looping;

public class Switch {
public static void main(String[] args) {
	int a =11;
	switch (a) {
	case 1:
		System.out.println("tamilnadu");
		break;
	case 2:
		System.out.println("karnataka");
        break;
	case 3:
		System.out.println("kerala");
		break;
	default:
		System.out.println("other country");
		break;
	}
}
}
